package edu.neu.csye6200.ca;

public class CACell {
	
	private int color;
	private int width;

	public CACell() {
		this.color = 0;
		this.width = 10;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
	
	
}
